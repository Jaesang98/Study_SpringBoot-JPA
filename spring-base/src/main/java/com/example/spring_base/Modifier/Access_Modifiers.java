package com.example.spring_base.Modifier;

/*
 * 접근제어자
 *  - public : 어디서나 사용가능
 *  - default : 폴더 내 사용가능
 *  - protected : 폴더 내 사용가능
 *  - private : 같은 클래스에서만 사용가능
 */

public class Access_Modifiers {
    public String public_str;
    String default_str;
    protected String protected_str;
    private String private_str;
}